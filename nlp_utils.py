def clean_text(text):
    text = text.lower()
    text = re.sub(r'[^\w\s]', '', text)
    return text.strip()

# 2. ПОДГОТОВКА И ОБУЧЕНИЕ МОДЕЛИ КЛАССИФИКАЦИИ (SVM)
X_text = []
y = []

for intent, intent_data in BOT_CONFIG['intents'].items():
    for example in intent_data['examples']:
        X_text.append(clean_text(example))
        y.append(intent)

# Используем символьные n-граммы от 2 до 4 символов, чтобы ИИ понимал опечатки и формы слов
vectorizer = TfidfVectorizer(analyzer='char_wb', ngram_range=(2, 4))
X = vectorizer.fit_transform(X_text)
clf = LinearSVC(dual=False)
clf.fit(X, y)

# 3. ПОДГОТОВКА ДАТАСЕТА DIALOGUES.TXT ДЛЯ "БОЛТАЛКИ"
dialogues_structured = {}

try:
    with open('dialogues.txt', 'r', encoding='utf-8') as f:
        content = f.read()
    
    dialogues_str = content.split('\n\n')
    dialogues = [dialogue_str.split('\n')[:2] for dialogue_str in dialogues_str]
    
    for dialogue in dialogues:
        if len(dialogue) != 2:
            continue
        
        question, answer = dialogue
        question_clean = clean_text(question[2:])
        answer_clean = answer[2:]
        
        if question_clean:
            words = set(question_clean.split(' '))
            for word in words:
                if word not in dialogues_structured:
                    dialogues_structured[word] = []
                dialogues_structured[word].append([question_clean, answer_clean])
                
    for word, pairs in dialogues_structured.items():
        dialogues_structured[word] = pairs[:1000]
        
except FileNotFoundError:
    print("ВНИМАНИЕ: Файл dialogues.txt не найден! Бот будет работать без болталки.")

# 4. ФУНКЦИИ РАСПОЗНАВАНИЯ И ГЕНЕРАЦИИ ОТВЕТА
def get_intent(text):
    """Классифицирует намерение через ML-модель."""
    text_clean = clean_text(text)
    if not text_clean:
        return None
        
    # Прямое предсказание ИИ-модели по векторам
    intent = clf.predict(vectorizer.transform([text_clean]))[0]
    
    # Считаем вероятность совпадения через решающую функцию классификатора
    # Если значение уверенности модели выше порога, отдаем интент! Вся жесткая логика Левенштейна убрана.
    decision = clf.decision_function(vectorizer.transform([text_clean]))[0]
    
    # Если классов много, decision_function вернет массив уверенностей для каждого интента.
    # Нам нужно убедиться, что ИИ действительно "уверен" в лучшем классе.
    if max(decision) > -0.5: 
        return intent
        
    return None

def get_dialogue_answer(text):
    """Ищет ответ в dialogues.txt, если интент не распознан."""
    text_clean = clean_text(text)
    words = set(text_clean.split(' '))
    mini_dataset = []
    
    for word in words:
        if word in dialogues_structured:
            mini_dataset.extend(dialogues_structured[word])
            
    answers = []
    for question, answer in mini_dataset:
        if abs(len(text_clean) - len(question)) / len(question) < 0.3:
            distance = nltk.edit_distance(text_clean, question)
            distance_weighted = distance / len(question)
            if distance_weighted < 0.3:
                answers.append([distance_weighted, answer])
                
    if answers:
        return min(answers, key=lambda x: x[0])[1]
    return None

def process_message(text):
    """Главная функция обработки текста."""
    # 1. Пытаемся понять бизнес-интент (ML)
    intent = get_intent(text)
    if intent:
        responses = BOT_CONFIG['intents'][intent].get('responses', [])
        if responses:
            return intent, random.choice(responses)
        return intent, None

    # 2. Если интент не понят, пытаемся просто поболтать по датасету (Алгоритм Левенштейна)
    dialogue_answer = get_dialogue_answer(text)
    if dialogue_answer:
        return "chit_chat", dialogue_answer

    # 3. Заглушка, плавно переводящая тему на обувь
    return "unknown", random.choice(BOT_CONFIG['failure_phrases'])