def clean_text(text):
    text = text.lower()
    text = re.sub(r'[^\w\s]', '', text)
    return text.strip()

X_text = []
y = []

for intent, intent_data in BOT_CONFIG['intents'].items():
    for example in intent_data['examples']:
        X_text.append(clean_text(example))
        y.append(intent)

vectorizer = TfidfVectorizer(analyzer='char_wb', ngram_range=(2, 4))
X = vectorizer.fit_transform(X_text)
clf = LinearSVC(dual=False)
clf.fit(X, y)

dialogues_dataset = []

def load_dialogues():
    """Загружает очищенный dialogues.txt в оперативную память"""
    global dialogues_dataset
    try:
        import os
        if os.path.exists('dialogues.txt'):
            with open('dialogues.txt', 'r', encoding='utf-8') as f:
                content = f.read()
            blocks = content.split('\n\n')
            for block in blocks:
                lines = [line.strip() for line in block.split('\n') if line.strip()]
                if len(lines) >= 2:
                    q = lines[0].replace('-', '').strip()
                    a = lines[1].replace('-', '').strip()
                    if q and a:
                        dialogues_dataset.append((clean_text(q), a))
            print(f"База болталки dialogues.txt успешно загружена! Пар: {len(dialogues_dataset)}")
    except Exception as e:
        print(f"Ошибка при загрузке dialogues.txt: {e}")

load_dialogues()

def get_intent(text):
    text_clean = clean_text(text)
    if not text_clean:
        return None
    intent = clf.predict(vectorizer.transform([text_clean]))[0]
    decision = clf.decision_function(vectorizer.transform([text_clean]))[0]

    if max(decision) > 0.1: 
        return intent
    return None

def get_dialogue_fallback(text, current_topic=None):
    """Поиск близкого по смыслу вопроса в датасете через расстояние Левенштейна"""
    text_clean = clean_text(text)
    if not text_clean or not dialogues_dataset:
        return None
        
    best_match_answer = None
    min_distance = 1000
    
    for question, answer in dialogues_dataset:
        distance = nltk.edit_distance(text_clean, question)
        
        # Если тема совпадает, уменьшаем дистанцию (приоритет)
        if current_topic and current_topic in question:
            distance -= 2 
            
        if distance < min_distance and distance <= 6:
            min_distance = distance
            best_match_answer = answer
            
    return best_match_answer

# --- НОВАЯ ВСТРОЕННАЯ ФУНКЦИЯ ДЛЯ УМНОГО ПЕРЕХВАТА ОБУВИ ---
def check_specific_shoe_request(text):
    """Проверяет, упомянул ли пользователь конкретный тип обуви из словаря"""
    t = text.lower()
    for key, formal_name in SUBCATEGORY_MAPPING.items():
        if key in t:
            return formal_name
    return None

import random

def process_message(text, allow_ad=False, topic=None):
    # 1. ПРИОРИТЕТ: Принудительная реклама (если allow_ad пришел True)
    # Это должно быть в начале, чтобы бот не "зацикливался" на болталке
    if allow_ad and hasattr(BOT_CONFIG, 'get') and 'ad_phrase' in BOT_CONFIG:
        return "ad", BOT_CONFIG['ad_phrase']

    # 2. ПРОБУЕМ НАЙТИ ОТВЕТ В БАЗЕ (Контекстная болталка)
    # Теперь мы передаем topic, чтобы поиск учитывал контекст
    chit_chat_answer = get_dialogue_fallback(text, current_topic=topic)
    if chit_chat_answer:
        return "chit_chat", chit_chat_answer

    # 3. КЛАССИФИКАТОР (Команды пользователя)
    intent = get_intent(text)
    if intent and intent in BOT_CONFIG.get('intents', {}):
        responses = BOT_CONFIG['intents'][intent].get('responses', [])
        if responses:
            # Если это покупка, отдаем 2-й вариант (рекламный), если есть
            if intent == 'buy_shoes' and len(responses) > 1:
                return intent, responses[1]
            return intent, responses[0]

    # 4. ЗАГЛУШКА (Если ничего не найдено)
    return "unknown", random.choice([
        "Понимаю! А чем ты тогда увлекаешься в свободное время?",
        "Ясно. А о чем тогда хочешь поболтать?",
        "Хорошо, давай сменим тему. Расскажешь что-нибудь интересное?"
    ])