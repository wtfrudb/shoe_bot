def clean_text(text):
    text = text.lower()
    text = re.sub(r'[^\w\s]', '', text)
    return text.strip()


X_text = []
y = []

for intent, intent_data in BOT_CONFIG['intents'].items():
    for example in intent_data['examples']:
        X_text.append(clean_text(example))
        y.append(intent)

vectorizer = TfidfVectorizer(analyzer='char_wb', ngram_range=(2, 4))
X = vectorizer.fit_transform(X_text)
clf = LinearSVC(dual=False)
clf.fit(X, y)


dialogues_dataset = []

def load_dialogues():
    """Загружает очищенный dialogues.txt в оперативную память"""
    global dialogues_dataset
    try:
        import os
        if os.path.exists('dialogues.txt'):
            with open('dialogues.txt', 'r', encoding='utf-8') as f:
                content = f.read()
            blocks = content.split('\n\n')
            for block in blocks:
                lines = [line.strip() for line in block.split('\n') if line.strip()]
                if len(lines) >= 2:
                    q = lines[0].replace('-', '').strip()
                    a = lines[1].replace('-', '').strip()
                    if q and a:
                        dialogues_dataset.append((clean_text(q), a))
            print(f"База болталки dialogues.txt успешно загружена! Пар: {len(dialogues_dataset)}")
    except Exception as e:
        print(f"Ошибка при загрузке dialogues.txt: {e}")


load_dialogues()

def get_intent(text):
    text_clean = clean_text(text)
    if not text_clean:
        return None
    intent = clf.predict(vectorizer.transform([text_clean]))[0]
    decision = clf.decision_function(vectorizer.transform([text_clean]))[0]

    if max(decision) > 0.1: 
        return intent
    return None

def get_dialogue_fallback(text):
    """Поиск близкого по смыслу вопроса в датасете через расстояние Левенштейна"""
    text_clean = clean_text(text)
    if not text_clean or not dialogues_dataset:
        return None
        
    best_match_answer = None
    min_distance = 1000
    
    for question, answer in dialogues_dataset:
        if abs(len(text_clean) - len(question)) > 5:
            continue
            
        distance = nltk.edit_distance(text_clean, question)
        if distance < min_distance and distance <= 3: 
            min_distance = distance
            best_match_answer = answer
            
        if min_distance == 0: 
            break
            
    return best_match_answer

def process_message(text, allow_ad=False):
    intent = get_intent(text)
    
    if intent and intent in BOT_CONFIG['intents']:
        responses = BOT_CONFIG['intents'][intent].get('responses', [])
        if responses:
            if allow_ad and len(responses) > 1:
                return intent, responses[1]
            return intent, responses[0]
        return intent, None

    chit_chat_answer = get_dialogue_fallback(text)
    if chit_chat_answer:
        return "chit_chat", chit_chat_answer

    if allow_ad:
        return "unknown", BOT_CONFIG['ad_failure_phrase']
    return "unknown", random.choice(BOT_CONFIG['failure_phrases'])