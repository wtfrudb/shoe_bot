def process_message(text, allow_ad=False, topic=None, last_bot_msg=None):
    # 1. ПРИОРИТЕТ: Принудительная реклама
    if allow_ad and 'ad_phrase' in BOT_CONFIG:
        return "ad", BOT_CONFIG['ad_phrase']

    # 2. КЛАССИФИКАТОР (Определяем намерение)
    intent = get_intent_with_keywords(text) # Используем новую функцию    
    
    if intent and intent in BOT_CONFIG.get('intents', {}):        
        responses = BOT_CONFIG['intents'][intent].get('responses', [])
        
        if responses:
            # Выбираем ответ (по умолчанию первый, если не покупка)
            if intent == 'buy_shoes' and len(responses) > 1:
                answer = responses[1]
            else:
                answer = responses[0]
            
            # ЗАЩИТА ОТ ЦИКЛА: 
            # Если бот хочет сказать то же самое, что сказал шаг назад,
            # пробуем взять другой вариант из списка или нейтральную фразу
            if answer == last_bot_msg and len(responses) > 1:
                answer = responses[1] if answer == responses[0] else responses[0]
            
            # Если после всех манипуляций он все еще хочет повториться, 
            # добавим "кстати" или смену темы
            if answer == last_bot_msg:
                answer = f"Кстати, это интересный момент! Но давай обсудим что-то еще. О чем ты хочешь поговорить?"
                
            return intent, answer

    # 3. ПРОБУЕМ НАЙТИ ОТВЕТ В БАЗЕ (Если это не команда, а обычная болтовня)
    chit_chat_answer = get_dialogue_fallback(text, current_topic=topic)
    if chit_chat_answer and chit_chat_answer != last_bot_msg:
        return "chit_chat", chit_chat_answer

    # 4. ЗАГЛУШКА (Если совсем ничего не понятно)
    fallback = random.choice([
        "Понимаю! А чем ты тогда увлекаешься в свободное время?",
        "Ясно. А о чем тогда хочешь поболтать?",
        "Хорошо, давай сменим тему. Расскажешь что-нибудь интересное?"
    ])
    
    # И тут проверяем, чтобы заглушка не повторялась
    if fallback == last_bot_msg:
        fallback = "Интересная мысль! Расскажи об этом поподробнее?"
        
    return "unknown", fallback