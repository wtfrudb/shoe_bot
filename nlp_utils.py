def check_specific_shoe_request(text):
    """Проверяет, упомянул ли пользователь конкретный тип обуви из словаря"""
    t = text.lower()
    for key, formal_name in SUBCATEGORY_MAPPING.items():
        if key in t:
            return formal_name
    return None

